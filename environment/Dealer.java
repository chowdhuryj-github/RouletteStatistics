
/**
 * the dealer class
 */
public class Dealer {

    // the attributes
    private final int coinPack;

    /**
     * constructor for the casino
     * @param coinPack
     */
    public Dealer(int coinPack) {
        this.coinPack = coinPack;
    }

    /**
     * method for returning number of coin packs
     * @return the coin packs
     */
    public int returnCoinPack() {
        return coinPack;
    }
    

}
